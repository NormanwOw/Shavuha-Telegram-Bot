async def basket_title(user_id: int) -> str:
    title = 'üõí <b>–ö–æ—Ä–∑–∏–Ω–∞</b>'
    time = await OrderDB.get_order_user_time(user_id)
    comment = await OrderDB.get_comment(user_id)
    if time is None:
        time = ''
    else:
        time = '\n‚è± ' + await OrderDB.get_order_user_time(user_id)

    if comment is None:
        comment = ''
    else:
        comment = '\n‚úè ' + comment

    return title + time + comment


async def order_comment_title(user_id: int) -> str:
    title = '<b>‚úè –ö–æ–º–º–µ–Ω—Ç–∞—Ä–∏–π –∫ –∑–∞–∫–∞–∑—É</b>'
    comment = await OrderDB.get_comment(user_id)
    if comment is None:
        comment = ''
    else:
        comment = '\n ' + comment
    return title + comment


